
package Models;

public class Datos {
    private int id;
    private int gestion;
    private int mes;
    private int aduana;
    private int depto;
    private int via;
    private int medio;
    private int pais;
    private int nandina;
    private int kilos;
    private int fob;
    private int adu;
    private int cusi;
    private int ident;
    public Datos(){
        
        }

    public Datos(int id, int gestion, int mes, int aduana, int depto, int via, int medio, int pais, int nandina, int kilos, int fob, int adu, int cusi, int ident) {
        this.id = id;
        this.gestion = gestion;
        this.mes = mes;
        this.aduana = aduana;
        this.depto = depto;
        this.via = via;
        this.medio = medio;
        this.pais = pais;
        this.nandina = nandina;
        this.kilos = kilos;
        this.fob = fob;
        this.adu = adu;
        this.cusi = cusi;
        this.ident = ident;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getGestion() {
        return gestion;
    }

    public void setGestion(int gestion) {
        this.gestion = gestion;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getAduana() {
        return aduana;
    }

    public void setAduana(int aduana) {
        this.aduana = aduana;
    }

    public int getDepto() {
        return depto;
    }

    public void setDepto(int depto) {
        this.depto = depto;
    }

    public int getVia() {
        return via;
    }

    public void setVia(int via) {
        this.via = via;
    }

    public int getMedio() {
        return medio;
    }

    public void setMedio(int medio) {
        this.medio = medio;
    }

    public int getPais() {
        return pais;
    }

    public void setPais(int pais) {
        this.pais = pais;
    }

    public int getNandina() {
        return nandina;
    }

    public void setNandina(int nandina) {
        this.nandina = nandina;
    }

    public int getKilos() {
        return kilos;
    }

    public void setKilos(int kilos) {
        this.kilos = kilos;
    }

    public int getFob() {
        return fob;
    }

    public void setFob(int fob) {
        this.fob = fob;
    }

    public int getAdu() {
        return adu;
    }

    public void setAdu(int adu) {
        this.adu = adu;
    }

    public int getCusi() {
        return cusi;
    }

    public void setCusi(int cusi) {
        this.cusi = cusi;
    }

    public int getIdent() {
        return ident;
    }

    public void setIdent(int ident) {
        this.ident = ident;
    }

   
}
